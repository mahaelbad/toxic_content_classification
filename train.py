import random

def augment_text(text):
    words = text.split()

    if len(words) < 4:
        return text

    aug_type = random.choice([
        "delete",
        "swap"
    ])

    if aug_type == "delete":
        idx = random.randint(0, len(words)-1)
        words.pop(idx)

    elif aug_type == "swap":
        idx1 = random.randint(0, len(words)-2)
        idx2 = idx1 + 1
        words[idx1], words[idx2] = words[idx2], words[idx1]

    return " ".join(words)

